-- DROP FUNCTION admin_platform.tenant_can_login(int4, timestamptz);

CREATE OR REPLACE FUNCTION admin_platform.tenant_can_login(p_tenant_id integer, p_now timestamp with time zone DEFAULT now())
 RETURNS TABLE(can_login boolean, reason text)
 LANGUAGE plpgsql
AS $function$
DECLARE
  tenant_record admin_platform.tenants%ROWTYPE;
BEGIN
  SELECT *
  INTO tenant_record
  FROM admin_platform.tenants t
  WHERE t.id = p_tenant_id
  LIMIT 1;

  IF NOT FOUND THEN
    RETURN QUERY SELECT false, 'tenant_not_found'::text;
    RETURN;
  END IF;

  IF tenant_record.estado = false THEN
    RETURN QUERY SELECT false, 'tenant_inactive'::text;
    RETURN;
  END IF;

  IF tenant_record.estado_suscripcion IN ('active', 'trialing') THEN
    RETURN QUERY SELECT true, 'ok'::text;
    RETURN;
  END IF;

  IF tenant_record.estado_suscripcion = 'past_due' THEN
    IF tenant_record.gracia_hasta IS NOT NULL AND tenant_record.gracia_hasta >= p_now THEN
      RETURN QUERY SELECT true, 'grace_period'::text;
      RETURN;
    END IF;

    RETURN QUERY SELECT false, 'payment_past_due'::text;
    RETURN;
  END IF;

  IF tenant_record.estado_suscripcion = 'paused' THEN
    RETURN QUERY SELECT false, 'subscription_paused'::text;
    RETURN;
  END IF;

  IF tenant_record.estado_suscripcion = 'canceled' THEN
    RETURN QUERY SELECT false, 'subscription_canceled'::text;
    RETURN;
  END IF;

  IF tenant_record.estado_suscripcion = 'unpaid' THEN
    RETURN QUERY SELECT false, 'subscription_unpaid'::text;
    RETURN;
  END IF;

  RETURN QUERY SELECT false, 'subscription_incomplete'::text;
END;
$function$
;

