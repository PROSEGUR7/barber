CREATE OR REPLACE FUNCTION admin_platform.sincronizar_estado_suscripciones_tenants(p_dias_gracia integer DEFAULT 5, p_dias_para_pausa integer DEFAULT 30)
 RETURNS TABLE(total_actualizados integer, a_past_due integer, a_unpaid integer, a_paused integer)
 LANGUAGE plpgsql
AS $function$
DECLARE
  v_past_due int := 0;
  v_unpaid int := 0;
  v_paused int := 0;
BEGIN
  UPDATE admin_platform.tenants t
  SET
    estado_suscripcion = 'past_due',
    gracia_hasta = now() + make_interval(days => p_dias_gracia)
  FROM admin_platform.suscripciones_tenants s
  WHERE s.tenant_id = t.id
    AND t.estado = true
    AND t.estado_suscripcion IN ('active', 'trialing')
    AND s.proximo_cobro IS NOT NULL
    AND s.proximo_cobro < now();
  GET DIAGNOSTICS v_past_due = ROW_COUNT;

  UPDATE admin_platform.tenants t
  SET estado_suscripcion = 'unpaid'
  WHERE t.estado_suscripcion = 'past_due'
    AND t.gracia_hasta IS NOT NULL
    AND t.gracia_hasta < now();
  GET DIAGNOSTICS v_unpaid = ROW_COUNT;

  UPDATE admin_platform.tenants t
  SET estado_suscripcion = 'paused'
  FROM admin_platform.suscripciones_tenants s
  WHERE s.tenant_id = t.id
    AND t.estado_suscripcion = 'unpaid'
    AND s.proximo_cobro IS NOT NULL
    AND s.proximo_cobro < now() - make_interval(days => p_dias_para_pausa);
  GET DIAGNOSTICS v_paused = ROW_COUNT;

  RETURN QUERY
  SELECT v_past_due + v_unpaid + v_paused, v_past_due, v_unpaid, v_paused;
END;
$function$
;

