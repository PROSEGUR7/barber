CREATE OR REPLACE FUNCTION admin_platform.actualizar_tenant(esquema_actual text, esquema_nuevo text, nuevo_nombre text, nuevo_correo text, nuevo_telefono text, "nueva_contraseña" text)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
DECLARE
  esquema_existe boolean;
  admin_id integer;
  has_col_correo boolean;
  has_col_nombre boolean;
  has_col_telefono boolean;
  has_col_passwordhash boolean;
  has_col_password boolean;
  has_col_ultima_actualizacion boolean;
  has_col_ultimo_acceso boolean;
  update_sql text;
BEGIN
  IF esquema_actual IS NULL OR btrim(esquema_actual) = '' THEN
    RAISE EXCEPTION 'esquema_actual no puede ser vacío.';
  END IF;

  IF esquema_nuevo IS NULL OR btrim(esquema_nuevo) = '' THEN
    RAISE EXCEPTION 'esquema_nuevo no puede ser vacío.';
  END IF;

  SELECT EXISTS (
    SELECT 1
    FROM information_schema.schemata
    WHERE schema_name = esquema_actual
  ) INTO esquema_existe;

  IF NOT esquema_existe THEN
    RAISE EXCEPTION 'El esquema % no existe.', esquema_actual;
  END IF;

  IF esquema_actual <> esquema_nuevo THEN
    IF lower(esquema_nuevo) IN ('public', 'admin_platform', 'tenant_base') THEN
      RAISE EXCEPTION 'No se puede renombrar a un esquema protegido (%).', esquema_nuevo;
    END IF;

    EXECUTE format('ALTER SCHEMA %I RENAME TO %I;', esquema_actual, esquema_nuevo);
  END IF;

  IF NOT EXISTS (
    SELECT 1
    FROM information_schema.tables
    WHERE table_schema = esquema_nuevo
      AND table_name = 'users'
  ) THEN
    RAISE EXCEPTION 'La tabla %.users no existe.', esquema_nuevo;
  END IF;

  EXECUTE format(
    'SELECT id FROM %I.users WHERE rol = ''admin'' ORDER BY id LIMIT 1;',
    esquema_nuevo
  ) INTO admin_id;

  IF admin_id IS NULL THEN
    RAISE EXCEPTION 'No hay usuario admin en %.users.', esquema_nuevo;
  END IF;

  SELECT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = esquema_nuevo AND table_name = 'users' AND column_name = 'correo'
  ) INTO has_col_correo;

  SELECT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = esquema_nuevo AND table_name = 'users' AND column_name = 'nombre'
  ) INTO has_col_nombre;

  SELECT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = esquema_nuevo AND table_name = 'users' AND column_name = 'telefono'
  ) INTO has_col_telefono;

  SELECT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = esquema_nuevo AND table_name = 'users' AND column_name = 'passwordhash'
  ) INTO has_col_passwordhash;

  SELECT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = esquema_nuevo AND table_name = 'users' AND column_name = 'password'
  ) INTO has_col_password;

  SELECT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = esquema_nuevo AND table_name = 'users' AND column_name = 'ultima_actualizacion'
  ) INTO has_col_ultima_actualizacion;

  SELECT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = esquema_nuevo AND table_name = 'users' AND column_name = 'ultimo_acceso'
  ) INTO has_col_ultimo_acceso;

  update_sql := format('UPDATE %I.users SET ', esquema_nuevo);

  IF has_col_correo THEN
    update_sql := update_sql || format('correo = %L, ', nuevo_correo);
  END IF;

  IF has_col_nombre THEN
    update_sql := update_sql || format('nombre = %L, ', nuevo_nombre);
  END IF;

  IF has_col_telefono THEN
    update_sql := update_sql || format('telefono = %L, ', nuevo_telefono);
  END IF;

  IF "nueva_contraseña" IS NOT NULL AND btrim("nueva_contraseña") <> '' THEN
    IF has_col_passwordhash THEN
      update_sql := update_sql || format('passwordhash = %L, ', "nueva_contraseña");
    ELSIF has_col_password THEN
      update_sql := update_sql || format('password = %L, ', "nueva_contraseña");
    END IF;
  END IF;

  IF has_col_ultima_actualizacion THEN
    update_sql := update_sql || 'ultima_actualizacion = now(), ';
  END IF;

  IF has_col_ultimo_acceso THEN
    update_sql := update_sql || 'ultimo_acceso = now(), ';
  END IF;

  update_sql := regexp_replace(update_sql, ',\s*$', '');
  update_sql := update_sql || format(' WHERE id = %s;', admin_id);

  EXECUTE update_sql;

  RAISE NOTICE 'Tenant % actualizado correctamente.', esquema_nuevo;
END;
$function$
;

