CREATE OR REPLACE FUNCTION admin_platform.crear_tenant(nuevo_esquema text, nombre_usuario text, correo_usuario text, telefono_usuario text, "contraseña_usuario" text)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
DECLARE
  table_rec record;
  enum_col_rec record;
  type_rec record;
  seq_rec record;
  seq_sql text;
  default_rec record;
  new_default text;
  seq_name text;
  fn_record record;
  fn_sql text;
  trigger_record record;
  trigger_sql text;
  fk_record record;
  fk_drop record;
  schema_prefix text;
BEGIN
  IF nuevo_esquema IS NULL OR btrim(nuevo_esquema) = '' THEN
    RAISE EXCEPTION 'El nombre del esquema no puede ser vacío.';
  END IF;

  IF lower(nuevo_esquema) IN ('public', 'admin_platform', 'tenant_base') THEN
    RAISE EXCEPTION 'El esquema % está protegido y no puede usarse.', nuevo_esquema;
  END IF;

  IF EXISTS (
    SELECT 1
    FROM information_schema.schemata
    WHERE schema_name = nuevo_esquema
  ) THEN
    RAISE EXCEPTION 'El esquema % ya existe.', nuevo_esquema;
  END IF;

  EXECUTE format('CREATE SCHEMA %I;', nuevo_esquema);
  schema_prefix := quote_ident(nuevo_esquema) || '.';

  FOR type_rec IN
    SELECT
      t.typname,
      string_agg(quote_literal(e.enumlabel), ', ' ORDER BY e.enumsortorder) AS labels
    FROM pg_type t
    JOIN pg_enum e ON e.enumtypid = t.oid
    JOIN pg_namespace n ON n.oid = t.typnamespace
    WHERE n.nspname = 'tenant_base'
    GROUP BY t.typname
  LOOP
    EXECUTE format('CREATE TYPE %I.%I AS ENUM (%s);', nuevo_esquema, type_rec.typname, type_rec.labels);
  END LOOP;

  FOR table_rec IN
    SELECT tablename
    FROM pg_tables
    WHERE schemaname = 'tenant_base'
    ORDER BY tablename
  LOOP
    EXECUTE format(
      'CREATE TABLE %I.%I (LIKE tenant_base.%I INCLUDING ALL);',
      nuevo_esquema,
      table_rec.tablename,
      table_rec.tablename
    );
  END LOOP;

  FOR enum_col_rec IN
    SELECT
      c.table_name,
      c.column_name,
      c.udt_name
    FROM information_schema.columns c
    WHERE c.table_schema = nuevo_esquema
      AND c.udt_schema = 'tenant_base'
      AND c.data_type = 'USER-DEFINED'
      AND EXISTS (
        SELECT 1
        FROM pg_type t
        JOIN pg_namespace n ON n.oid = t.typnamespace
        WHERE n.nspname = nuevo_esquema
          AND t.typname = c.udt_name
      )
  LOOP
    EXECUTE format(
      'ALTER TABLE %I.%I ALTER COLUMN %I DROP DEFAULT;',
      nuevo_esquema,
      enum_col_rec.table_name,
      enum_col_rec.column_name
    );

    EXECUTE format(
      'ALTER TABLE %I.%I ALTER COLUMN %I TYPE %I.%I USING (%I::text::%I.%I);',
      nuevo_esquema,
      enum_col_rec.table_name,
      enum_col_rec.column_name,
      nuevo_esquema,
      enum_col_rec.udt_name,
      enum_col_rec.column_name,
      nuevo_esquema,
      enum_col_rec.udt_name
    );
  END LOOP;

  FOR seq_rec IN
    SELECT
      sequencename,
      data_type,
      start_value,
      increment_by,
      min_value,
      max_value,
      cache_size,
      cycle
    FROM pg_sequences
    WHERE schemaname = 'tenant_base'
  LOOP
    seq_sql := format(
      'CREATE SEQUENCE IF NOT EXISTS %I.%I AS %s INCREMENT BY %s START WITH %s CACHE %s ',
      nuevo_esquema,
      seq_rec.sequencename,
      seq_rec.data_type,
      seq_rec.increment_by,
      seq_rec.start_value,
      seq_rec.cache_size
    );

    IF seq_rec.min_value IS NULL THEN
      seq_sql := seq_sql || 'NO MINVALUE ';
    ELSE
      seq_sql := seq_sql || format('MINVALUE %s ', seq_rec.min_value);
    END IF;

    IF seq_rec.max_value IS NULL THEN
      seq_sql := seq_sql || 'NO MAXVALUE ';
    ELSE
      seq_sql := seq_sql || format('MAXVALUE %s ', seq_rec.max_value);
    END IF;

    IF seq_rec.cycle THEN
      seq_sql := seq_sql || 'CYCLE;';
    ELSE
      seq_sql := seq_sql || 'NO CYCLE;';
    END IF;

    EXECUTE seq_sql;
  END LOOP;

  FOR default_rec IN
    SELECT table_name, column_name, column_default
    FROM information_schema.columns
    WHERE table_schema = nuevo_esquema
      AND column_default LIKE '%tenant_base.%'
  LOOP
    new_default := replace(default_rec.column_default, 'tenant_base.', schema_prefix);

    EXECUTE format(
      'ALTER TABLE %I.%I ALTER COLUMN %I SET DEFAULT %s;',
      nuevo_esquema,
      default_rec.table_name,
      default_rec.column_name,
      new_default
    );

    SELECT pg_get_serial_sequence(format('%I.%I', nuevo_esquema, default_rec.table_name), default_rec.column_name)
    INTO seq_name;

    IF seq_name IS NOT NULL THEN
      EXECUTE format(
        'ALTER SEQUENCE %s OWNED BY %I.%I.%I;',
        seq_name,
        nuevo_esquema,
        default_rec.table_name,
        default_rec.column_name
      );
    END IF;
  END LOOP;

  FOR fn_record IN
    SELECT
      p.oid,
      format('%I.%I', n.nspname, p.proname) AS qualified_name,
      pg_get_functiondef(p.oid) AS definition
    FROM pg_proc p
    JOIN pg_namespace n ON n.oid = p.pronamespace
    WHERE n.nspname = 'tenant_base'
      AND p.prokind = 'f'
  LOOP
    fn_sql := replace(fn_record.definition, 'tenant_base.', schema_prefix);
    fn_sql := regexp_replace(
      fn_sql,
      'CREATE\s+OR\s+REPLACE\s+FUNCTION\s+tenant_base\.',
      'CREATE OR REPLACE FUNCTION ' || quote_ident(nuevo_esquema) || '.',
      1,
      1,
      'i'
    );
    EXECUTE fn_sql;
  END LOOP;

  FOR trigger_record IN
    SELECT
      tg.oid,
      tg.tgname,
      rel.relname AS table_name,
      pg_get_triggerdef(tg.oid, true) AS definition
    FROM pg_trigger tg
    JOIN pg_class rel ON rel.oid = tg.tgrelid
    JOIN pg_namespace nsp ON nsp.oid = rel.relnamespace
    WHERE nsp.nspname = 'tenant_base'
      AND tg.tgisinternal = false
  LOOP
    trigger_sql := replace(trigger_record.definition, 'tenant_base.', schema_prefix) || ';';
    EXECUTE trigger_sql;
  END LOOP;

  FOR fk_drop IN
    SELECT con.conname AS constraint_name, rel.relname AS table_name
    FROM pg_constraint con
    JOIN pg_class rel ON rel.oid = con.conrelid
    JOIN pg_namespace nsp ON nsp.oid = rel.relnamespace
    WHERE nsp.nspname = nuevo_esquema
      AND con.contype = 'f'
  LOOP
    EXECUTE format(
      'ALTER TABLE %I.%I DROP CONSTRAINT %I;',
      nuevo_esquema,
      fk_drop.table_name,
      fk_drop.constraint_name
    );
  END LOOP;

  FOR fk_record IN
    SELECT
      con.conname AS constraint_name,
      rel.relname AS table_name,
      confrel.relname AS foreign_table_name,
      string_agg(quote_ident(att.attname), ', ' ORDER BY k.ord) AS local_columns,
      string_agg(quote_ident(confatt.attname), ', ' ORDER BY k.ord) AS foreign_columns,
      CASE con.confdeltype
        WHEN 'r' THEN 'RESTRICT'
        WHEN 'c' THEN 'CASCADE'
        WHEN 'n' THEN 'SET NULL'
        WHEN 'd' THEN 'SET DEFAULT'
        ELSE 'NO ACTION'
      END AS on_delete_action,
      CASE con.confupdtype
        WHEN 'r' THEN 'RESTRICT'
        WHEN 'c' THEN 'CASCADE'
        WHEN 'n' THEN 'SET NULL'
        WHEN 'd' THEN 'SET DEFAULT'
        ELSE 'NO ACTION'
      END AS on_update_action
    FROM pg_constraint con
    JOIN pg_class rel ON rel.oid = con.conrelid
    JOIN pg_namespace nsp ON nsp.oid = rel.relnamespace
    JOIN pg_class confrel ON confrel.oid = con.confrelid
    JOIN LATERAL unnest(con.conkey) WITH ORDINALITY AS k(attnum, ord) ON true
    JOIN pg_attribute att ON att.attrelid = con.conrelid AND att.attnum = k.attnum
    JOIN LATERAL unnest(con.confkey) WITH ORDINALITY AS fk(attnum, ord) ON fk.ord = k.ord
    JOIN pg_attribute confatt ON confatt.attrelid = con.confrelid AND confatt.attnum = fk.attnum
    WHERE nsp.nspname = 'tenant_base'
      AND con.contype = 'f'
    GROUP BY con.conname, rel.relname, confrel.relname, con.confdeltype, con.confupdtype
    ORDER BY rel.relname, con.conname
  LOOP
    EXECUTE format(
      'ALTER TABLE %I.%I ADD CONSTRAINT %I FOREIGN KEY (%s) REFERENCES %I.%I (%s) ON DELETE %s ON UPDATE %s;',
      nuevo_esquema,
      fk_record.table_name,
      fk_record.constraint_name,
      fk_record.local_columns,
      nuevo_esquema,
      fk_record.foreign_table_name,
      fk_record.foreign_columns,
      fk_record.on_delete_action,
      fk_record.on_update_action
    );
  END LOOP;

  IF EXISTS (
    SELECT 1
    FROM information_schema.tables
    WHERE table_schema = nuevo_esquema
      AND table_name = 'users'
  ) THEN
    BEGIN
      EXECUTE format(
        'INSERT INTO %I.users (correo, passwordhash, rol, estado, fecha_creacion, ultima_actualizacion)
         VALUES ($1, $2, ''admin''::%I.user_role_enum, ''activo''::%I.user_status_enum, now(), now());',
        nuevo_esquema,
        nuevo_esquema,
        nuevo_esquema
      ) USING correo_usuario, "contraseña_usuario";
    EXCEPTION WHEN OTHERS THEN
      EXECUTE format(
        'INSERT INTO %I.users (correo, passwordhash, rol, estado)
         VALUES ($1, $2, ''admin''::%I.user_role_enum, ''activo''::%I.user_status_enum);',
        nuevo_esquema,
        nuevo_esquema,
        nuevo_esquema
      ) USING correo_usuario, "contraseña_usuario";
    END;
  ELSE
    RAISE EXCEPTION 'No existe %.users en el esquema creado.', nuevo_esquema;
  END IF;

  RAISE NOTICE 'Tenant % creado correctamente desde tenant_base.', nuevo_esquema;
END;
$function$
;

