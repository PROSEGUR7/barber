-- DROP FUNCTION admin_platform.registrar_pago_tenant(int4, numeric, text, text, text, text, admin_platform.ciclo_facturacion_enum, timestamptz);

CREATE OR REPLACE FUNCTION admin_platform.registrar_pago_tenant(p_tenant_id integer, p_monto numeric, p_moneda text DEFAULT 'COP'::text, p_metodo_pago text DEFAULT NULL::text, p_proveedor_pago text DEFAULT NULL::text, p_referencia_externa text DEFAULT NULL::text, p_ciclo admin_platform.ciclo_facturacion_enum DEFAULT NULL::admin_platform.ciclo_facturacion_enum, p_fecha_pago timestamp with time zone DEFAULT now())
 RETURNS TABLE(pago_id integer, factura_id integer, suscripcion_id integer, tenant_id integer, estado_suscripcion admin_platform.tenant_billing_status_enum, proximo_cobro timestamp with time zone)
 LANGUAGE plpgsql
AS $function$
DECLARE
  v_suscripcion admin_platform.suscripciones_tenants%ROWTYPE;
  v_ciclo admin_platform.ciclo_facturacion_enum;
  v_plan admin_platform.planes_suscripcion%ROWTYPE;
  v_numero_factura text;
  v_factura_id int;
  v_pago_id int;
  v_inicio_periodo timestamptz;
  v_fin_periodo timestamptz;
  v_monto_ciclo numeric(12,2);
BEGIN
  IF p_monto <= 0 THEN
    RAISE EXCEPTION 'El monto del pago debe ser mayor a 0.';
  END IF;

  SELECT *
  INTO v_suscripcion
  FROM admin_platform.suscripciones_tenants s
  WHERE s.tenant_id = p_tenant_id
  LIMIT 1;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'No existe suscripción para tenant_id=%.', p_tenant_id;
  END IF;

  SELECT *
  INTO v_plan
  FROM admin_platform.planes_suscripcion p
  WHERE p.id = v_suscripcion.plan_id
  LIMIT 1;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'No existe plan para la suscripción tenant_id=%.', p_tenant_id;
  END IF;

  v_ciclo := COALESCE(p_ciclo, v_suscripcion.ciclo_facturacion);

  v_monto_ciclo := CASE v_ciclo
    WHEN 'mensual' THEN v_plan.precio_mensual
    WHEN 'trimestral' THEN v_plan.precio_trimestral
    ELSE v_plan.precio_anual
  END;

  v_inicio_periodo := CASE
    WHEN v_suscripcion.fecha_fin_periodo > p_fecha_pago THEN v_suscripcion.fecha_fin_periodo
    ELSE p_fecha_pago
  END;

  v_fin_periodo := v_inicio_periodo + admin_platform.fn_intervalo_por_ciclo(v_ciclo);

  v_numero_factura := format('FAC-%s-%s', to_char(now(), 'YYYYMMDDHH24MISSUS'), p_tenant_id);

  INSERT INTO admin_platform.facturas_tenants (
    tenant_id,
    suscripcion_id,
    numero_factura,
    estado,
    fecha_emision,
    fecha_vencimiento,
    periodo_inicio,
    periodo_fin,
    subtotal,
    impuestos,
    descuento,
    moneda,
    pagada_en,
    detalle
  )
  VALUES (
    p_tenant_id,
    v_suscripcion.id,
    v_numero_factura,
    'pagada',
    p_fecha_pago,
    p_fecha_pago,
    v_inicio_periodo,
    v_fin_periodo,
    p_monto,
    0,
    0,
    COALESCE(NULLIF(p_moneda, ''), v_suscripcion.moneda),
    p_fecha_pago,
    jsonb_build_object('origen', 'manual_o_pasarela', 'ciclo_aplicado', v_ciclo)
  )
  RETURNING id INTO v_factura_id;

  INSERT INTO admin_platform.pagos_tenants (
    tenant_id,
    suscripcion_id,
    factura_id,
    estado,
    monto,
    moneda,
    metodo_pago,
    proveedor_pago,
    referencia_externa,
    pagado_en,
    payload_pasarela
  )
  VALUES (
    p_tenant_id,
    v_suscripcion.id,
    v_factura_id,
    'aprobado',
    p_monto,
    COALESCE(NULLIF(p_moneda, ''), v_suscripcion.moneda),
    p_metodo_pago,
    p_proveedor_pago,
    p_referencia_externa,
    p_fecha_pago,
    jsonb_build_object('referencia', p_referencia_externa)
  )
  RETURNING id INTO v_pago_id;

  UPDATE admin_platform.suscripciones_tenants
  SET
    ciclo_facturacion = v_ciclo,
    monto_ciclo = v_monto_ciclo,
    moneda = COALESCE(NULLIF(p_moneda, ''), v_suscripcion.moneda),
    fecha_inicio_periodo = v_inicio_periodo,
    fecha_fin_periodo = v_fin_periodo,
    proximo_cobro = v_fin_periodo,
    fecha_actualizacion = now()
  WHERE id = v_suscripcion.id;

  UPDATE admin_platform.tenants
  SET
    estado_suscripcion = 'active',
    gracia_hasta = NULL,
    ultimo_pago_en = p_fecha_pago
  WHERE id = p_tenant_id;

  RETURN QUERY
  SELECT
    v_pago_id,
    v_factura_id,
    v_suscripcion.id,
    p_tenant_id,
    'active'::admin_platform.tenant_billing_status_enum,
    v_fin_periodo;
END;
$function$
;

